package topic2;

public class HelloForLoop {
        public static void main(String[] args) {
            // A for loop that initializes x to 0, runs as long as x is less than 10, and increments x by 1 each iteration
            for (int x = 0; x < 10; x++) {
                System.out.println(x); // Print the current value of x
            }
        }

}
